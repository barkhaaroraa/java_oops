public class wrapper {
    public static void main(String args[])
    {
        String s1=new String("String 1");
        StringBuffer s2=new StringBuffer("STRING 2");
        System.out.println("first string "+s1);
        System.out.println("second string "+s2);


        System.out.println("lowercase first string "+s1.toLowerCase());
        System.out.println("lowercase second string "+s2.toString().toLowerCase());

        String str2 = "Hello, World!";
        String str1 = "Hello, Worldffffffffffff!";
        System.out.println(str1);
        System.out.println(str2);


        
    }
    
}
